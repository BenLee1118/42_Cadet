#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	i;
	size_t			length;
	char			*dest;

	if (!s || !f)
		return (0);
	i = 0;
	length = ft_strlen(s);
	dest = (char *)malloc((length + 1) * sizeof(char));
	if (!dest)
		return (0);
	while (s[i] != '\0')
	{
		dest[i] = (*f)(i, s[i]);
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
