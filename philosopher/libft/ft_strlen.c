#include "libft.h"

size_t	ft_strlen(const char *str)
{
	size_t	i;
	size_t	a;

	i = 0;
	a = 0;
	while (*(str + i) != '\0')
	{
		a = a + 1;
		i++;
	}
	return (i);
}
